package com.antonioleiva.mvpexample.mvp.presenter.impl;

import com.antonioleiva.mvpexample.mvp.model.LoginModel;
import com.antonioleiva.mvpexample.mvp.model.impl.LoginModelImpl;
import com.antonioleiva.mvpexample.mvp.presenter.LoginPresenter;
import com.antonioleiva.mvpexample.mvp.presenter.OnLoginListener;
import com.antonioleiva.mvpexample.mvp.ui.view.LoginView;

public class LoginPresenterImpl implements LoginPresenter, OnLoginListener {

    private LoginView loginView;
    private LoginModel loginInteractor;
//构造函数
    public LoginPresenterImpl(LoginView loginView) {
        this.loginView = loginView;
        this.loginInteractor = new LoginModelImpl();
    }






//LoginPresenter父类方法
    @Override
    public void validateCredentials(String username, String password) {
        if (loginView != null) {
            loginView.showProgress();
        }

        loginInteractor.login(username, password, this);
    }
//LoginPresenter父类方法
    @Override
    public void onDestroy() {
        loginView = null;
    }







//OnLoginListener父类方法
    @Override
    public void onUsernameError() {
        if (loginView != null) {
            loginView.setUsernameError();
            loginView.hideProgress();
        }
    }
//OnLoginListener父类方法
    @Override
    public void onPasswordError() {
        if (loginView != null) {
            loginView.setPasswordError();
            loginView.hideProgress();
        }
    }
//OnLoginListener父类方法
    @Override
    public void onSuccess() {
        if (loginView != null) {
            loginView.navigateToHome();
        }
    }
}
